                                                <div class="paragraph-row">
                                                    <div class="column9">
                                                        <img style='width:100%' src='http://portal.malangkab.go.id/uploads/artikel/malangkab-pusat-opd@3507-fb533f1a-7ff8-4b1a-b43d-66e8610a8bcd.jpeg'/>
                                                        </a><br>
                                                        
                                                        <br/>
                                                                <p>KALIPARE - Wakil Bupati Malang Drs.H.Didik Gatot Subroto,SH.,M.H, mendampingi Kunjungan Kerja Menteri Ketenagakerjaan RI&nbsp;Dr. Hj. Ida Fauziyah, M.Si.,&nbsp;ke Desa Migran Produktif (DESMIGRATIF) di Desa Arjowilangun Kecamatan Kalipare, Kabupaten Malang pada Kamis (29/8) sore. Selain sebagai bentuk perhatian dan kepedulian Pemerintah Pusat terhadap masyarakat di wilayah, kehadiran Menteri Ketenagakerjaan dapat memberikan dorongan dalam pentingnya memberdayakan Pekerja Migran Indonesia (CPMI) di desa yang menjadi kantong-kantong Pekerja Migran Indonesia terutama pada desa migran produktif (Desmigratif) Arjowilangun kecamatan Kalipare.</p>

                                                                <p>Dalam sambutannya, Wakil Bupati Malang menyampaikan Terima kasih kepada rombongan Menteri Ketenagakerjaan RI atas atensi dan perhatian yang telah diberikan kepada masyarakat khususnya desa Arjowilangun Kecamatan Kalipare serta masyarakat Kabupaten Malang pada umumnya, utamanya bagi para Calon Pekerja Migran Indonesia (CPMI),&nbsp; Pekerja Migran Indonesia (PMI) Purna, Dan Keluarga Pekerja Migran Indonesia (PMI).</p>

                                                                <p>Sebagaimana telah kita ketahui bersama bahwasanya Kabupaten Malang merupakan salah satu daerah kantong Pekerja Migran Indonesia, terdapat 5 desa&nbsp; migran produktif. Di kecamatan Kalipare ada 2 desa, yaitu desa Arjowilangun dan Desa Sumberpetung. Kecamatan Pagelaran, Desa Brongkal dan Balearjo. Serta desa Gedangan di Kecamatan Gedangan.</p>

                                                                <p>5 Desa migran produktif ini masih didominasi oleh pekerja informal, untuk itu perlu adanya upaya penguatkan program pemberdayaan Pekerja Migran Indonesia (PMI), baik yang telah purna maupun keluarganya. Dimana diharapkan program tersebut dapat mengurangi permasalahan dan menyejahterakan Pekerja Migran Indonesia (PMI) beserta keluarganya, sehingga mengurangi keinginan untuk kembali bekerja di luar negeri.</p>

                                                                <p>&quot;Maka pada kesempatan ini, kami menyampaikan terima kasih dan apresiasi yang setinggi-tingginya kepada seluruh pihak yang telah berkontribusi terhadap upaya terobosan dalam memberdayakan, meningkatkan pelayanan serta memberi perlindungan bagi Calon Pekerja Migran Indonesia (CPMI) di desa yang menjadi kantong-kantong Pekerja Migran Indonesia dengan mengembangkan Desa Migran Produktif (Desmigratif) di Kabupaten Malang ini,&quot; ujar Wakil Bupati Malang.</p>

                                                                <p>Sementara itu, Menteri Ketenagakerjaan&nbsp;(Menaker RI)&nbsp;Ida Fauziyah menyebutkan, bahwa desa merupakan garda terdepan perlindungan bagi para<a href="https://www.malangtimes.com/tag/pekerja-migran-indonesia">&nbsp;Pekerja Migran Indonesia&nbsp;</a>(PMI) yang akan berangkat bekerja ke luar negeri.&nbsp;Setiap pembuat kebijakan memiliki kewajiban memberikan perlindungan kepada para pekerja migran Indonesia.&nbsp;Mulai dari pemerintah pusat, pemerintah provinsi, pemerintah kabupaten/kota hingga pemerintah desa/kelurahan.</p>

                                                                <p>Selain itu, Menaker RI Ida juga mengatakan, bahwa saat ini Kementerian Ketenagakerjaan RI memiliki program&nbsp;<a href="https://www.malangtimes.com/tag/desa-migran-produktif">Desa Migran Produktif.</a>&nbsp;Di mana Program Desa Migran Produktif yang salah satunya ada di Desa Arjowilangun ini bertujuan untuk memfasilitasi masyarakat yang berniat berangkat bekerja ke luar negeri,&quot;setiap pekerja migran Indonesia merupakan pahlawan devisa negara yang harus dihormati dan dihargai,&quot; imbuhnya.<br />
                                                                <br />
                                                                Pihaknya pun menegaskan, bahwa hal itu tidak boleh hanya slogan saja, tetapi juga harus dibuktikan secara nyata dalam hal perlindungan yang paripurna kepada setiap pekerja migran Indonesia.&nbsp;&quot;Perlindungan dimulai dari desa. Maka desa harus memberikan layanan kepada calon pekerja migran Indonesia sampai purna,&quot; tutur Menaker Ida.</p>

                                                                <p>
    <br />
Lebih lanjut, pihaknya berharap agar masyarakat yang akan berangkat bekerja ke luar negeri dapat memahami segala macam informasi agar terhindar dari Perusahaan Penyalur Pekerja Migran Indonesia atau P3MI abal-abal atau palsu.&nbsp;<br />
<br />
&quot;Harapannya tidak ada lagi orang yang ingin bekerja melalui perusahaan penyalur PMI yang abal-abal. Karena semua bisa dibuka melalui teknologi dan sistem yang kita bangun,&quot; pungkas Menaker RI dihadapan awak media. (prokopim/dhe)<br />
&nbsp;</p>



